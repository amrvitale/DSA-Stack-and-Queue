/*
Queue = data structure that models first in, first out (FIFO) operation
Line up, service is provided in order of customers lined up
Data is inserted at end, removed at front

Example: event loop of a web browser, handled in order they entered the queue

two primary functions:
enqueue(data): adds data to queue = Insertion operation
dequeue(): removes oldest data added to queue = Deletion operation

*/

