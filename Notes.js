/*
Stack = data structure similar to list, but access is restricted to only 1 end
Last in first out (LIFO) order
think like a stack of plates, last one is on top of stack, but first one you grab
think vertical structure

two primary functions: 
push(): places data on top of a stack = Insertion operation

pop(): removes data from top of stack = Removal operation

*/


